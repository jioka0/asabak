from sqlalchemy import create_engine
from sqlalchemy.ext.declarative import declarative_base
from sqlalchemy.orm import sessionmaker
import os

from core.config import settings

# Database configuration - using SQLite for local development
DATABASE_URL = os.getenv("DATABASE_URL", settings.database_url)

engine = create_engine(
    DATABASE_URL,
    connect_args={"check_same_thread": False} if DATABASE_URL.startswith("sqlite") else {}
)
SessionLocal = sessionmaker(autocommit=False, autoflush=False, bind=engine)

Base = declarative_base()

# Import essential models to ensure tables are created
# This must be after Base is defined to avoid circular imports
from models.blog import BlogPost, BlogComment, BlogLike, TemporalUser, BlogTag

# Create tables
def create_tables():
    # Create tables with checkfirst=True to avoid errors if tables already exist
    try:
        print("üöÄ Creating database tables...")
        Base.metadata.create_all(bind=engine, checkfirst=True)
        print("‚úÖ Database tables created/verified successfully!")
        
        # List all tables that should exist
        from sqlalchemy import inspect
        inspector = inspect(engine)
        tables = inspector.get_table_names()
        print(f"üìã Existing tables: {tables}")
        
        if 'blog_likes' in tables:
            print("‚úÖ blog_likes table exists!")
        else:
            print("‚ùå blog_likes table is missing!")
            
        if 'blog_tags' in tables:
            print("‚úÖ blog_tags table exists!")
        else:
            print("‚ùå blog_tags table is missing!")
            
    except Exception as e:
        print(f"‚ùå Error creating tables: {str(e)}")
        raise

# Dependency to get database session
def get_db():
    db = SessionLocal()
    try:
        yield db
    finally:
        db.close()